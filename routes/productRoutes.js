const express = require('express');
const router = express.Router();
const Product = require('../models/productModel');

const getBaseUrl = () => {
  return process.env.BASE_URL || 'http://localhost:3000';
};

// Create product
router.post('/', async (req, res) => {
  try {
    const { name, description, adminName } = req.body;
    const baseUrl = getBaseUrl();
    const shareLink = `${baseUrl}/feedback.html?product=${encodeURIComponent(name)}`;

    const newProduct = new Product({
      name,
      description,
      createdBy: adminName,
      shareLink
    });

    const savedProduct = await newProduct.save();
    res.status(201).json(savedProduct);
  } catch (error) {
    res.status(500).json({ message: 'Server error', error: error.message });
  }
});

// Get all products
router.get('/', async (req, res) => {
  try {
    const products = await Product.find();
    res.json(products);
  } catch (error) {
    res.status(500).json({ message: 'Server error', error: error.message });
  }
});

// Get single product
router.get('/:id', async (req, res) => {
  try {
    const product = await Product.findById(req.params.id);
    if (!product) {
      return res.status(404).json({ message: 'Product not found' });
    }
    res.json(product);
  } catch (error) {
    res.status(500).json({ message: 'Server error', error: error.message });
  }
});

// Update product
router.put('/:id', async (req, res) => {
  try {
    const { name, description } = req.body;
    const product = await Product.findById(req.params.id);
    
    if (!product) {
      return res.status(404).json({ message: 'Product not found' });
    }

    product.name = name || product.name;
    product.description = description || product.description;
    
    // Update share link with new name
    const baseUrl = getBaseUrl();
    product.shareLink = `${baseUrl}/feedback.html?product=${encodeURIComponent(product.name)}`;

    await product.save();
    res.json(product);
  } catch (error) {
    res.status(500).json({ message: 'Server error', error: error.message });
  }
});

// Delete product
router.delete('/:id', async (req, res) => {
  try {
    const product = await Product.findByIdAndDelete(req.params.id);
    if (!product) {
      return res.status(404).json({ message: 'Product not found' });
    }
    res.json({ message: 'Product deleted successfully' });
  } catch (error) {
    res.status(500).json({ message: 'Server error', error: error.message });
  }
});

module.exports = router;
